def test_evaluate_proposal_effect_marks_improved_after_window():
    proposal = {
        "proposal_id": "proposal_0004_001",
        "suggestion_id": "suggestion_0004_001",
        "cycle_id": 4,
        "source": "review.param_adjustment",
        "patch": {"position_size": 0.14},
        "metadata": {"proposal_kind": "param_adjustment"},
        "adoption_status": "adopted_to_candidate",
        "adoption_ref": {"decision_cycle_id": 4},
        "effect_window": {
            "window_cycles": 3,
            "start_cycle_id": 5,
            "end_cycle_id": 7,
            "evaluation_after_cycle_id": 7,
        },
        "effect_target_metrics": ["avg_return_pct", "benchmark_pass_rate"],
        "effect_status": "pending",
        "effect_result": {"status": "pending", "observed_cycles": 0, "summary": ""},
    }
    cycle_history = [
        {"cycle_id": 2, "return_pct": -0.8, "benchmark_passed": False},
        {"cycle_id": 3, "return_pct": -0.2, "benchmark_passed": False},
        {"cycle_id": 4, "return_pct": 0.1, "benchmark_passed": False},
        {"cycle_id": 5, "return_pct": 1.2, "benchmark_passed": True},
        {"cycle_id": 6, "return_pct": 0.9, "benchmark_passed": True},
        {"cycle_id": 7, "return_pct": 0.8, "benchmark_passed": True},
    ]

    evaluated = evaluate_proposal_effect(
        proposal,
        cycle_history=cycle_history,
        current_cycle_id=7,
    )

    assert evaluated["effect_status"] == "improved"
    assert evaluated["effect_result"]["status"] == "improved"
    assert evaluated["effect_result"]["observed_cycles"] == 3
    assert {
        item["metric"] for item in evaluated["effect_result"]["metric_results"]
    } == {"avg_return_pct", "benchmark_pass_rate"}


