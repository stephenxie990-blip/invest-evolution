def test_candidate_builder_merges_review_and_optimization_proposals_into_single_candidate(tmp_path: Path):
    config_path = _repo_model_config("momentum_v1.yaml")
    appended = []
    logs = []
    controller = SimpleNamespace(
        output_dir=tmp_path,
        model_name="momentum",
        model_config_path=config_path,
        auto_apply_mutation=False,
        current_params={"position_size": 0.2, "take_profit_pct": 0.15},
        current_cycle_learning_proposals=[
            _proposal(
                "proposal_0007_001",
                source="optimization.llm_analysis",
                patch={"position_size": 0.12},
                proposal_kind="runtime_param_adjustment",
            ),
            _proposal(
                "proposal_0007_002",
                source="optimization.llm_scoring",
                patch={"weights": {"change_5d": 0.25}},
                proposal_kind="scoring_adjustment",
            ),
            _proposal(
                "proposal_0007_003",
                source="review.agent_weight_adjustment",
                patch={"trend_hunter": 0.9},
                proposal_kind="agent_weight_adjustment",
            ),
            _proposal(
                "proposal_0007_004",
                source="review.param_adjustment",
                patch={"position_size": 0.14},
                proposal_kind="param_adjustment",
            ),
        ],
        model_mutator=YamlConfigMutator(generations_dir=tmp_path / "generations"),
        cycle_history=[],
        _append_optimization_event=lambda event: appended.append(event.to_dict()),
        _emit_module_log=lambda *args, **kwargs: logs.append((args, kwargs)),
        _reload_investment_model=lambda path: None,
    )
    bundle = persist_cycle_proposal_bundle(
        controller,
        cycle_id=7,
        execution_snapshot={
            "active_config_ref": config_path,
            "active_version_id": "version_active_1",
            "runtime_fingerprint": "fingerprint_active_1",
            "model_name": "momentum",
        },
    )

    event = build_cycle_candidate_from_proposals(
        controller,
        cycle_id=7,
        proposal_bundle=bundle,
        event_factory=Event,
        trigger_reason="cycle_review_completed",
    )
    payload = event.to_dict()
    candidate_path = Path(payload["decision"]["config_path"])
    generated = yaml.safe_load(candidate_path.read_text(encoding="utf-8"))

    assert payload["stage"] == "candidate_build"
    assert payload["decision"]["proposal_bundle_id"] == bundle["proposal_bundle_id"]
    assert payload["lineage"]["candidate_config_ref"] == str(candidate_path)
    assert payload["applied_change"]["proposal_refs"] == [
        "proposal_0007_003",
        "proposal_0007_004",
    ]
    assert generated["params"]["position_size"] == 0.14
    assert generated["summary_scoring"]["weights"]["change_5d"] == 0.15
    assert generated["agent_weights"]["trend_hunter"] == 0.9
    assert appended[-1]["stage"] == "candidate_build"
    assert any(kwargs.get("kind") == "candidate_build" for _, kwargs in logs)
    gate = payload["evidence"]["proposal_gate"]
    assert gate["proposal_summary"]["approved_proposal_count"] == 2
    assert gate["proposal_summary"]["blocked_proposal_count"] == 2
    assert set(gate["proposal_summary"]["top_block_reasons"]) == {
        "single_step_identity_drift_exceeded",
        "single_step_scoring_identity_drift_exceeded",
    }
    assert gate["blocked_proposals"][0]["proposal_id"] == "proposal_0007_001"
    assert gate["approved_proposals"][-1]["proposal_id"] == "proposal_0007_004"
    tracked_bundle = load_cycle_proposal_bundle(bundle["bundle_path"])
    assert tracked_bundle["suggestion_tracking_summary"]["adopted_suggestion_count"] == 2
    assert tracked_bundle["suggestion_tracking_summary"]["rejected_suggestion_count"] == 2
    tracked_by_id = {
        item["proposal_id"]: item
        for item in tracked_bundle["proposals"]
    }
    assert tracked_by_id["proposal_0007_003"]["adoption_status"] == "adopted_to_candidate"
    assert tracked_by_id["proposal_0007_003"]["effect_status"] == "pending"
    assert tracked_by_id["proposal_0007_001"]["adoption_status"] == "rejected_by_proposal_gate"
    assert tracked_by_id["proposal_0007_001"]["effect_status"] == "not_applicable"


